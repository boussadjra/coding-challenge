    {

        $n = [];
        foreach ($request->neighbors as $key => $neighbor) {
            $_node = Node::where('x', '=', $neighbor['x'])->where('y', '=', $neighbor['y'])->first();

            NodeNeighbor::create([
                'id_node' => $request->node['id'],
                'id_node_neighbor' => $_node['id'],
            ]);

        }
        return $n;
    }

